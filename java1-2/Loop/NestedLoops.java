import java.util.Scanner;

public class NestedLoops  {
    
    public static void main(String[] args) {
    
      
      for(int x=7;x>0;x--)
      {  
         
         for(int y=1;y<=x;y++)
         {
            System.out.print("*");
         }
         System.out.println();
      }
    }
}
